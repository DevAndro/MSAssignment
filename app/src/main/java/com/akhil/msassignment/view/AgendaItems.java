package com.akhil.msassignment.view;

public class AgendaItems {
    private String mDate;
    private String mAgenda;
    private String mLocation;
    private String mWeatherInfo;

    public String getDate() {
        return mDate;
    }

    public void setDate(String mDate) {
        this.mDate = mDate;
    }

    public String getAgenda() {
        return mAgenda;
    }

    public void setAgenda(String mAgenda) {
        this.mAgenda = mAgenda;
    }

    public String getLocation() {
        return mLocation;
    }

    public void setLocation(String mLocation) {
        this.mLocation = mLocation;
    }

    public String getWeatherInfo() {
        return mWeatherInfo;
    }

    public void setWeatherInfo(String mWeatherInfo) {
        this.mWeatherInfo = mWeatherInfo;
    }
}